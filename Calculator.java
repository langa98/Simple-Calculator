    private void jbtnEqualSignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnEqualSignActionPerformed
        NumEnter2 = Double.parseDouble(jtxtResults.getText());
        if(op=="+"){
            Results = NumEnter1 + NumEnter2;
            jtxtResults.setText(String.valueOf(Results));
        }
        else if(op == "-"){
            Results = NumEnter1 - NumEnter2;
            jtxtResults.setText(String.valueOf(Results));
        }
        else if(op == "*"){
            Results = NumEnter1 * NumEnter2;
            jtxtResults.setText(String.valueOf(Results));
        }
        else if(op=="/"){
            Results = NumEnter1 / NumEnter2;
            jtxtResults.setText(String.valueOf(Results));
        }
    }//GEN-LAST:event_jbtnEqualSignActionPerformed

